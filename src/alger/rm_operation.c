/*--------------------------------------------------------------------------------------------*/
/* \brief Product of vector by scalar y = a.x */
/*!
 * [in]  Scalar a
 * [in]  Vector x
 * [in]  vector size   
 * [out] Vector y
 !*/
void
scalVect(const double a,
	 const double x[],
	 const int    size,
	 double       y[])
  
{
  for (int i = 0; i < size; i++) {
    y[i] = a*x[i];
  }

}

 /*--------------------------------------------------------------------------------------------*/
/* Sum of two vectors z = x + y*/
/*!
 * [in]  Vector x
 * [in]  Vector y
 * [in]  vector size   
 * [out] Vector z
 !*/
void
sum(const double x[],
    const double y[],
    const int    size,
    double       z[])
{
  
  for (int i = 0; i < size; i++) {
    double sum = 0;
    sum = x[i] + y[i];
    z[i] = sum;
  }

}


/*--------------------------------------------------------------------------------------------*/
/* Return the dot product of two vectors a.b */
/*!
 * [in] vector a
 * [in] vector b
 * [in] size n
 !*/
double
dot_prod(const double   a[],
	 const double   b[],
	 const int      size)
{
  double sum = 0;
  for (int i = 0; i < size; i ++)
    sum += a[i]*b[i];

  return sum;
}
  
/*--------------------------------------------------------------------------------------------*/

/* Return the norm LÂ² of vector a */
/*!
 * [in] vector a
 * [in] size n
 !*/
double
normL2(const double   a[],
       const int      size)

{
  double norm = sqrt(dot_prod(a,a,size));
  return norm;
}
