#ifndef __RM_GRADIENT_H__
#define __RM_GRADIENT_H__



/*----------------------------------------------------------------------------------------------*/
/*! 
 * In this file we defines the different types of gradient for solve the linear systeme Ax = b
 !*/
/*----------------------------------------------------------------------------------------------*/


/*----------------------------------------------------------------------------------------------*/
/*! 
 *C  headers
 !*/
/*----------------------------------------------------------------------------------------------*/
#include <stdio.h>

/*----------------------------------------------------------------------------------------------*/
/*! 
 *Local  headers
 !*/
/*----------------------------------------------------------------------------------------------*/
#include "rm_matrix_1D.h"
/*----------------------------------------------------------------------------------------------*/




/*----------------------------------------------------------------------------------------------*/
/* \brief gradient fixe step, x <- x -step*gradient.
 *  Here gradient = Ax - b */
/*!
 * [out]   Vector     x
 * [in]    Vector     b
 * [in]    Matrix     A
 !*/
void
gradientFix(double              x[],
	    const double        b[],
	    const rm_matrix_1D  *A);


/*----------------------------------------------------------------------------------------------*/
/* \brief gradient optimal step, x <- x -stepk*gradient.
 *  Here gradient = Ax - b */
/*!
 * [out]   Vector     x
 * [in]    Vector     b
 * [in]    Matrix     A
 !*/
void
gradientOpti(double              x[],
	     const double        b[],
	     const rm_matrix_1D  *A);

/*----------------------------------------------------------------------------------------------*/
/* \brief conjugate gradient */
/*!
 * [out]   Vector     x
 * [in]    Vector     b
 * [in]    Matrix     A
 !*/
void
gradientConju(double              x[],
	      const double        b[],
	      const rm_matrix_1D  *A);


/*----------------------------------------------------------------------------------------------*/
#endif /* __RM_GRADIENT_H__ */
