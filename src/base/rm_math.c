/*--------------------------------------------------------------------------------------*/
/* rm_pow2: return a square of a double */
/*!
 * [in] double x
 * Return x²
 !*/
double
rm_pow2(double x)
{
  return x*x;
}

/*--------------------------------------------------------------------------------------*/

/* rm_pow3 : return a cube of a double */
/*!
 * [in] double x
 * Return x³
 !*/
/*--------------------------------------------------------------------------------------*/

double
rm_pow3(double x)
{
  return x*x*x;
}

/*--------------------------------------------------------------------------------------*/

/* rm_pow4: return a power 4  of a double */
/*!
 * [in] double x
 * Return x⁴
 !*/

double
rm_pow4(double x)
{
  return x*x*x*x;
}


/*--------------------------------------------------------------------------------------*/
/*!
 * Compute the absolute value of a real value.
 *
 * [in]  x  value
 *
 * \return the real of the given value
 */
/*--------------------------------------------------------------------------------------*/
double rm_abs(double x)
{
  double abs = (x <  0) ? -x : x;
  return abs;
}

